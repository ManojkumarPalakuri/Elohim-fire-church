/**
 * Look up Telugu verses for a given book and chapter.
 * Returns an array of { v, t } objects matching the main app's format,
 * or an empty array if no data exists for that chapter.
 */
export function getTeluguChapter(bookId, chapter) {
    const normalizedId = bookId.replace(/\s+/g, '');
    const bookData = TELUGU_BIBLE[normalizedId] || TELUGU_BIBLE[bookId];
    if (!bookData) return [];
    return (bookData[chapter] || []).map(entry => ({
        v: entry.verse_number,
        t: entry.text,
    }));
}
